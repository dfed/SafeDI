	@Test
	@available(macOS 13.0, iOS 16.0, tvOS 16.0, watchOS 9.0, *)
	mutating func mock_existingMockMethodInDeepTree() async throws {
		// Grandchild has a user-defined mock with parameters. Parent → Child → Grandchild.
		// The construction chain should call Grandchild.mock() at the deepest level.
		let output = try await executeSafeDIToolTest(
			swiftFileContent: [
				"""
				@Instantiable(generateMock: true)
				public struct Service: Instantiable {
				    public init() {}
				}
				""",
				"""
				@Instantiable(generateMock: true, customMockName: "customMock")
				public struct Grandchild: Instantiable {
				    public init(service: Service) {
				        self.service = service
				    }
				    @Received let service: Service

				    public static func customMock(service: Service) -> Grandchild {
				        Grandchild(service: service)
				    }
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct Child: Instantiable {
				    public init(grandchild: Grandchild) {
				        self.grandchild = grandchild
				    }
				    @Instantiated let grandchild: Grandchild
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct Parent: Instantiable {
				    public init(child: Child) {
				        self.child = child
				    }
				    @Instantiated let child: Child
				}
				""",
			],
			buildSwiftOutputDirectory: true,
			filesToDelete: &filesToDelete,
		)

		// Parent → Child → Grandchild.mock(service:)
		#expect(output.mockFiles["Parent+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension Parent {
		    public struct SafeDIParameters {
		        public struct Service_Configuration {
		            public init(
		                _ safeDIBuilder: (() -> Service)? = nil
		            ) {
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let safeDIBuilder: (() -> Service)?
		        }

		        public struct Grandchild_Configuration {
		            public init(
		                _ safeDIBuilder: ((Service) -> Grandchild)? = nil
		            ) {
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let safeDIBuilder: ((Service) -> Grandchild)?
		        }

		        public struct Child_Configuration {
		            public init(
		                grandchild: Grandchild_Configuration = .init(),
		                _ safeDIBuilder: ((Grandchild) -> Child)? = nil
		            ) {
		                self.grandchild = grandchild
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let grandchild: Grandchild_Configuration
		            public let safeDIBuilder: ((Grandchild) -> Child)?
		        }

		        public init(
		            service: Service_Configuration = .init(),
		            child: Child_Configuration = .init()
		        ) {
		            self.service = service
		            self.child = child
		        }

		        public let service: Service_Configuration
		        public let child: Child_Configuration
		    }

		    public static func mock(
		        safeDIParameters: SafeDIParameters = .init()
		    ) -> Parent {
		        let service = (safeDIParameters.service.safeDIBuilder ?? Service.init)()
		        let grandchild = (safeDIParameters.child.grandchild.safeDIBuilder ?? Grandchild.customMock(service:))(service)
		        let child = (safeDIParameters.child.safeDIBuilder ?? Child.init(grandchild:))(grandchild)
		        return Parent(child: child)
		    }
		}
		#endif
		""", "Unexpected output \(output.mockFiles["Parent+SafeDIMock.swift"] ?? "")")
	}

