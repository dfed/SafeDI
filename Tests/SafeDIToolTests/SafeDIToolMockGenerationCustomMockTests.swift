	@Test
	@available(macOS 13.0, iOS 16.0, tvOS 16.0, watchOS 9.0, *)
	mutating func mock_usesExistingMockMethodInChildConstruction() async throws {
		// When a child type has a user-defined mock() with parameters matching its
		// dependencies, the parent's generated mock calls Child.mock(...) instead
		// of Child(...).
		let output = try await executeSafeDIToolTest(
			swiftFileContent: [
				"""
				@Instantiable(generateMock: true)
				public struct Dependency: Instantiable {
				    public init() {}
				}
				""",
				"""
				@Instantiable(generateMock: true, customMockName: "customMock")
				public struct Child: Instantiable {
				    public init(dependency: Dependency) {
				        self.dependency = dependency
				    }
				    @Received let dependency: Dependency

				    public static func customMock(dependency: Dependency) -> Child {
				        Child(dependency: dependency)
				    }
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct Parent: Instantiable {
				    public init(child: Child) {
				        self.child = child
				    }
				    @Instantiated let child: Child
				}
				""",
			],
			buildSwiftOutputDirectory: true,
			filesToDelete: &filesToDelete,
		)

		// Parent's mock should call Child.mock(dependency:) not Child(dependency:).
		#expect(output.mockFiles["Parent+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension Parent {
		    public struct SafeDIParameters {
		        public struct Dependency_Configuration {
		            public init(
		                _ safeDIBuilder: (() -> Dependency)? = nil
		            ) {
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let safeDIBuilder: (() -> Dependency)?
		        }

		        public struct Child_Configuration {
		            public init(
		                _ safeDIBuilder: ((Dependency) -> Child)? = nil
		            ) {
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let safeDIBuilder: ((Dependency) -> Child)?
		        }

		        public init(
		            dependency: Dependency_Configuration = .init(),
		            child: Child_Configuration = .init()
		        ) {
		            self.dependency = dependency
		            self.child = child
		        }

		        public let dependency: Dependency_Configuration
		        public let child: Child_Configuration
		    }

		    public static func mock(
		        safeDIParameters: SafeDIParameters = .init()
		    ) -> Parent {
		        let dependency = (safeDIParameters.dependency.safeDIBuilder ?? Dependency.init)()
		        let child = (safeDIParameters.child.safeDIBuilder ?? Child.customMock(dependency:))(dependency)
		        return Parent(child: child)
		    }
		}
		#endif
		""", "Unexpected output \(output.mockFiles["Parent+SafeDIMock.swift"] ?? "")")

		// Child has generateMock: true and a hand-written mock — generated mock calls through.
		#expect(output.mockFiles["Child+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension Child {
		    public struct SafeDIParameters {
		        public struct Dependency_Configuration {
		            public init(
		                _ safeDIBuilder: (() -> Dependency)? = nil
		            ) {
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let safeDIBuilder: (() -> Dependency)?
		        }

		        public init(
		            dependency: Dependency_Configuration = .init()
		        ) {
		            self.dependency = dependency
		        }

		        public let dependency: Dependency_Configuration
		    }

		    public static func mock(
		        safeDIParameters: SafeDIParameters = .init()
		    ) -> Child {
		        let dependency = (safeDIParameters.dependency.safeDIBuilder ?? Dependency.init)()
		        return Child.customMock(dependency: dependency)
		    }
		}
		#endif
		""", "Unexpected output \(output.mockFiles["Child+SafeDIMock.swift"] ?? "")")
	}

	@Test
