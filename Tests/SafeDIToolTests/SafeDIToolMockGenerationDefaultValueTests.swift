	@Test
	@available(macOS 13.0, iOS 16.0, tvOS 16.0, watchOS 9.0, *)
	mutating func mock_defaultValuedParamDoesNotSuppressReceivedPropertyBinding() async throws {
		// A child has a default-valued init param with the same label as a received
		// dependency on another child. The default-valued declaration must NOT suppress
		// the received property's root-level binding.
		let output = try await executeSafeDIToolTest(
			swiftFileContent: [
				"""
				@Instantiable(isRoot: true, generateMock: true)
				public struct Root: Instantiable {
				    public init(childA: ChildA, childB: ChildB) {
				        self.childA = childA
				        self.childB = childB
				    }
				    @Instantiated let childA: ChildA
				    @Instantiated let childB: ChildB
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct ChildA: Instantiable {
				    public init(service: Service?) {
				        self.service = service
				    }
				    @Received(onlyIfAvailable: true) let service: Service?
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct ChildB: Instantiable {
				    public init(service: Service? = nil) {}
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct Service: Instantiable {
				    public init() {}
				}
				""",
			],
			buildSwiftOutputDirectory: true,
			filesToDelete: &filesToDelete,
		)

		// ChildA receives `service` (onlyIfAvailable). ChildB has `service` as a
		// default-valued param. The root-level binding for `service` must exist
		// so ChildA's construction can reference the resolved value, not the closure.
		#expect(output.mockFiles["Root+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension Root {
		    public struct SafeDIParameters {
		        public struct ChildA_Configuration {
		            public init(
		                _ safeDIBuilder: ((Service?) -> ChildA)? = nil
		            ) {
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let safeDIBuilder: ((Service?) -> ChildA)?
		        }

		        public struct ChildB_Configuration {
		            public init(
		                service: Service? = nil,
		                _ safeDIBuilder: ((Service?) -> ChildB)? = nil
		            ) {
		                self.service = service
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let service: Service?
		            public let safeDIBuilder: ((Service?) -> ChildB)?
		        }

		        public init(
		            childA: ChildA_Configuration = .init(),
		            childB: ChildB_Configuration = .init()
		        ) {
		            self.childA = childA
		            self.childB = childB
		        }

		        public let childA: ChildA_Configuration
		        public let childB: ChildB_Configuration
		    }

		    public static func mock(
		        service: Service? = nil,
		        safeDIParameters: SafeDIParameters = .init()
		    ) -> Root {
		        let childA = (safeDIParameters.childA.safeDIBuilder ?? ChildA.init(service:))(service)
		        let childB = (safeDIParameters.childB.safeDIBuilder ?? ChildB.init(service:))(safeDIParameters.childB.service)
		        return Root(childA: childA, childB: childB)
		    }
		}
		#endif
		""", "Unexpected output \(output.mockFiles["Root+SafeDIMock.swift"] ?? "")")
	}

