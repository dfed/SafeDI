	@Test
	@available(macOS 13.0, iOS 16.0, tvOS 16.0, watchOS 9.0, *)
	mutating func mock_defaultValuedParameterBubblesUpToRootMock() async throws {
		let output = try await executeSafeDIToolTest(
			swiftFileContent: [
				"""
				@Instantiable(isRoot: true, generateMock: true)
				public struct Root: Instantiable {
				    public init(child: Child, shared: Shared) {
				        self.child = child
				        self.shared = shared
				    }
				    @Instantiated let child: Child
				    @Instantiated let shared: Shared
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct Child: Instantiable {
				    public init(shared: Shared, flag: Bool = false) {
				        self.shared = shared
				    }
				    @Received let shared: Shared
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct Shared: Instantiable {
				    public init() {}
				}
				""",
			],
			buildSwiftOutputDirectory: true,
			filesToDelete: &filesToDelete,
		)

		#expect(output.mockFiles.count == 3)
		#expect(output.mockFiles["Child+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension Child {
		    public struct SafeDIParameters {
		        public struct Shared_Configuration {
		            public init(
		                _ safeDIBuilder: (() -> Shared)? = nil
		            ) {
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let safeDIBuilder: (() -> Shared)?
		        }

		        public init(
		            shared: Shared_Configuration = .init()
		        ) {
		            self.shared = shared
		        }

		        public let shared: Shared_Configuration
		    }

		    public static func mock(
		        flag: Bool = false,
		        safeDIParameters: SafeDIParameters = .init()
		    ) -> Child {
		        let shared = (safeDIParameters.shared.safeDIBuilder ?? Shared.init)()
		        return Child(shared: shared, flag: flag)
		    }
		}
		#endif
		""", "Unexpected output \(output.mockFiles["Child+SafeDIMock.swift"] ?? "")")

		#expect(output.mockFiles["Root+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension Root {
		    public struct SafeDIParameters {
		        public struct Shared_Configuration {
		            public init(
		                _ safeDIBuilder: (() -> Shared)? = nil
		            ) {
		                self.safeDIBuilder = safeDIBuilder
		            }

		            public let safeDIBuilder: (() -> Shared)?
		        }

