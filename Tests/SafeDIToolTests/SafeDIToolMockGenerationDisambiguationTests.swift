	@Test
	@available(macOS 13.0, iOS 16.0, tvOS 16.0, watchOS 9.0, *)
	mutating func mock_disambiguationUsesSimplifiedSuffixWhenUnique() async throws {
		// Two children have `service` with different types. One is optional.
		// Simplified suffixes (stripping ?) are unique → use simplified.
		// service: ExternalService → service_ExternalService (not service_ExternalService)
		// service: LocalService? → service_LocalService (not service_LocalService_Optional)
		let output = try await executeSafeDIToolTest(
			swiftFileContent: [
				"""
				@Instantiable(generateMock: true)
				public struct Root: Instantiable {
				    public init(childA: ChildA, childB: ChildB) {
				        self.childA = childA
				        self.childB = childB
				    }
				    @Instantiated let childA: ChildA
				    @Instantiated let childB: ChildB
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct ChildA: Instantiable {
				    public init(service: ExternalService) { self.service = service }
				    @Received let service: ExternalService
				}
				""",
				"""
				@Instantiable(generateMock: true)
				public struct ChildB: Instantiable {
				    public init(service: LocalService?) { self.service = service }
				    @Received(onlyIfAvailable: true) let service: LocalService?
				}
				""",
				"""
				public struct ExternalService {}
				""",
				"""
				public struct LocalService {}
				""",
			],
			buildSwiftOutputDirectory: true,
			filesToDelete: &filesToDelete,
		)

		#expect(output.mockFiles["Root+SafeDIMock.swift"] == """
