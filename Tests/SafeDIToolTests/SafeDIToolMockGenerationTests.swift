	@Test
	mutating func mock_receivedErasedTypeAutoWraps() async throws {
		let output = try await executeSafeDIToolTest(
			swiftFileContent: [
				"""
				public class AnyService {
				    public init(_ service: some Any) {}
				}
				""",
				"""
				@Instantiable(isRoot: true)
				public struct Root: Instantiable {
				    public init(child: Child, myService: AnyService) {
				        self.child = child
				        self.myService = myService
				    }
				    @Instantiated let child: Child
				    @Instantiated(fulfilledByType: "ConcreteService", erasedToConcreteExistential: true) let myService: AnyService
				}
				""",
				"""
				@Instantiable
				public struct Child: Instantiable {
				    public init(myService: AnyService) {
				        self.myService = myService
				    }
				    @Received let myService: AnyService
				}
				""",
				"""
				@Instantiable
				public struct ConcreteService: Instantiable {
				    public init() {}
				}
				""",
			],
			buildSwiftOutputDirectory: true,
			filesToDelete: &filesToDelete,
			enableMockGeneration: true,
		)

		// Child receives AnyService, which is erased-to-concrete.
		// The mock should auto-detect this and provide AnyService param with inline wrapping.
		#expect(output.mockFiles["Child+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension Child {
		    public enum SafeDIMockPath {
		        public enum AnyService { case parent }
		    }

		    public static func mock(
		        anyService: ((SafeDIMockPath.AnyService) -> AnyService)? = nil
		    ) -> Child {
		        let anyService = anyService?(.parent) ?? AnyService(ConcreteService.mock())
		        return Child(myService: anyService)
		    }
		}
		#endif
		""")
	}

