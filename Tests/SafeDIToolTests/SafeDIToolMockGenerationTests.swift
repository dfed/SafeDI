	@Test
	mutating func mock_generatedForFullTree() async throws {
		let output = try await executeSafeDIToolTest(
			swiftFileContent: [
				"""
				@Instantiable(isRoot: true)
				public struct Root: Instantiable {
				    public init(childA: ChildA, shared: SharedThing) {
				        self.childA = childA
				        self.shared = shared
				    }
				    @Instantiated let childA: ChildA
				    @Instantiated let shared: SharedThing
				}
				""",
				"""
				@Instantiable
				public struct ChildA: Instantiable {
				    public init(shared: SharedThing, grandchild: Grandchild) {
				        self.shared = shared
				        self.grandchild = grandchild
				    }
				    @Received let shared: SharedThing
				    @Instantiated let grandchild: Grandchild
				}
				""",
				"""
				@Instantiable
				public struct Grandchild: Instantiable {
				    public init(shared: SharedThing) {
				        self.shared = shared
				    }
				    @Received let shared: SharedThing
				}
				""",
				"""
				@Instantiable
				public struct SharedThing: Instantiable {
				    public init() {}
				}
				""",
			],
			buildSwiftOutputDirectory: true,
			filesToDelete: &filesToDelete,
			enableMockGeneration: true,
		)

		#expect(output.mockFiles.count == 4)
		#expect(output.mockFiles["ChildA+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension ChildA {
		    public enum SafeDIMockPath {
		        public enum Grandchild { case root }
		        public enum SharedThing { case parent }
		    }

		    public static func mock(
		        grandchild: ((SafeDIMockPath.Grandchild) -> Grandchild)? = nil,
		        shared: ((SafeDIMockPath.SharedThing) -> SharedThing)? = nil
		    ) -> ChildA {
		        let shared = shared?(.parent) ?? SharedThing()
		        let grandchild = grandchild?(.root) ?? Grandchild(shared: shared)
		        return ChildA(shared: shared, grandchild: grandchild)
		    }
		}
		#endif
		""")

		#expect(output.mockFiles["Grandchild+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension Grandchild {
		    public enum SafeDIMockPath {
		        public enum SharedThing { case parent }
		    }

		    public static func mock(
		        shared: ((SafeDIMockPath.SharedThing) -> SharedThing)? = nil
		    ) -> Grandchild {
		        let shared = shared?(.parent) ?? SharedThing()
		        return Grandchild(shared: shared)
		    }
		}
		#endif
		""")

		#expect(output.mockFiles["Root+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension Root {
		    public enum SafeDIMockPath {
		        public enum ChildA { case root }
		        public enum Grandchild { case childA }
		        public enum SharedThing { case root }
		    }

		    public static func mock(
		        childA: ((SafeDIMockPath.ChildA) -> ChildA)? = nil,
		        grandchild: ((SafeDIMockPath.Grandchild) -> Grandchild)? = nil,
		        shared: ((SafeDIMockPath.SharedThing) -> SharedThing)? = nil
		    ) -> Root {
		        let shared = shared?(.root) ?? SharedThing()
		        func __safeDI_childA() -> ChildA {
		            let grandchild = grandchild?(.root) ?? Grandchild(shared: shared)
		            return ChildA(shared: shared, grandchild: grandchild)
		        }
		        let childA: ChildA = childA?(.root) ?? __safeDI_childA()
		        return Root(childA: childA, shared: shared)
		    }
		}
		#endif
		""")

		#expect(output.mockFiles["SharedThing+SafeDIMock.swift"] == """
		// This file was generated by the SafeDIGenerateDependencyTree build tool plugin.
		// Any modifications made to this file will be overwritten on subsequent builds.
		// Please refrain from editing this file directly.

		#if DEBUG
		extension SharedThing {
		    public static func mock() -> SharedThing {
		        SharedThing()
		    }
		}
		#endif
		""")
	}

