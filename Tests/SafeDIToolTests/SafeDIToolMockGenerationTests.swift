	@Test
	mutating func mock_notGeneratedWhenNoConfigurationExists() async throws {
		let output = try await executeSafeDIToolTest(
			swiftFileContent: [
				"""
				@Instantiable
				public struct SimpleType: Instantiable {
				    public init() {}
				}
				""",
